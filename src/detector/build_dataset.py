"""
Step 1 — Build the binary 'lesion' detector dataset for ONE experiment arm.

Two arms, identical code, differing ONLY in training data:

  original_only  (control)   — pool/ + ~570 negatives + locked test/
  plus_roboflow  (treatment) — control  +  converted Roboflow train+valid

Invariants enforced here:
  * Originals (pool/, test/, Normal/, additional/) are read-only.
  * The pool/ train/val split is computed ONCE (POOL_SPLIT_JSON) and reused
    byte-identically by both arms and by Step 3 — the ONLY allowed cross-arm
    difference is the extra Roboflow images appended to treatment's train.
  * Detector labels are single-class 0. Disease classes live only in the
    sidecar (provenance for a future classifier phase — unused this run).
  * web_holdout/ (Roboflow test splits + a seeded slice of Roboflow valid)
    is EVAL-ONLY and built identically in both arms; never trained on.
  * No Roboflow image ever reaches the classifier in this change.

Conversion + dedup run in BOTH arms (fast, deterministic) so web_holdout,
the dedup report and the sidecar exist regardless of which arm is built.
"""

from __future__ import annotations

import json
import shutil
from collections import Counter
from pathlib import Path

from sklearn.model_selection import train_test_split

import config
from src.common.dedup import build_report, write_report
from src.common.io import label_path_for, list_images, read_yolo_label
from src.detector.convert_roboflow import (
    DATASETS,
    SPLITS,
    convert_split,
    valid_holdout_stems,
)


# ── pool helpers (unchanged behaviour) ────────────────────────────────────────

def _majority_class(label_path: Path) -> int:
    rows = read_yolo_label(label_path)
    if not rows:
        return -1
    counts = Counter(cid for cid, *_ in rows)
    top = max(counts.values())
    return min(cid for cid, n in counts.items() if n == top)


def _binary_label_text(src_label: Path) -> str:
    rows = read_yolo_label(src_label)
    return "\n".join(
        f"0 {cx:.6f} {cy:.6f} {w:.6f} {h:.6f}" for _, cx, cy, w, h in rows
    )


def _load_or_create_pool_split() -> dict:
    """Compute the pool train/val split once; thereafter always reload it so
    both arms (and Step 3) see a byte-identical split."""
    if config.POOL_SPLIT_JSON.exists():
        return json.loads(config.POOL_SPLIT_JSON.read_text())

    pool_images = list_images(config.POOL_IMAGES)
    strata = [
        _majority_class(label_path_for(p, config.POOL_LABELS))
        for p in pool_images
    ]
    train_idx, val_idx = train_test_split(
        range(len(pool_images)),
        test_size=config.VAL_FRACTION,
        stratify=strata,
        random_state=config.SPLIT_SEED,
    )
    train_set = set(train_idx)
    split = {
        "train": sorted(
            pool_images[i].stem for i in range(len(pool_images))
            if i in train_set
        ),
        "val": sorted(
            pool_images[i].stem for i in range(len(pool_images))
            if i not in train_set
        ),
    }
    config.POOL_SPLIT_JSON.parent.mkdir(parents=True, exist_ok=True)
    config.POOL_SPLIT_JSON.write_text(json.dumps(split, indent=2))
    return split


# ── YOLO tree scaffolding ─────────────────────────────────────────────────────

def _fresh_tree(root: Path, splits=("train", "val", "test")) -> None:
    if root.exists():
        shutil.rmtree(root)
    for s in splits:
        (root / "images" / s).mkdir(parents=True)
        (root / "labels" / s).mkdir(parents=True)


def _place(root: Path, split: str, img: Path, label_text: str,
           dst_name: str | None = None) -> None:
    name = dst_name or img.name
    stem = Path(name).stem
    shutil.copy2(img, root / "images" / split / name)
    (root / "labels" / split / f"{stem}.txt").write_text(label_text)


def _write_data_yaml(root: Path, layout: dict[str, str]) -> None:
    body = (
        "# Auto-generated by src/detector/build_dataset.py — do not hand-edit.\n"
        f"path: {root.resolve()}\n"
    )
    for k, v in layout.items():
        body += f"{k}: {v}\n"
    body += "nc: 1\nnames: [lesion]\n"
    (root / "data.yaml").write_text(body)


# ── build ─────────────────────────────────────────────────────────────────────

def build(arm: str) -> dict:
    if arm not in config.ARMS:
        raise ValueError(f"unknown arm {arm!r}; expected one of {config.ARMS}")

    root = config.det_dataset_dir(arm)
    _fresh_tree(root)
    config.NEW_DATA_ROOT.mkdir(parents=True, exist_ok=True)

    # ── pool (stable split, binary labels) ───────────────────────────────────
    split = _load_or_create_pool_split()
    stem_to_split = {s: "train" for s in split["train"]}
    stem_to_split.update({s: "val" for s in split["val"]})
    for img in list_images(config.POOL_IMAGES):
        sp = stem_to_split.get(img.stem)
        if sp is None:
            continue
        _place(root, sp, img,
                _binary_label_text(label_path_for(img, config.POOL_LABELS)))

    # ── negatives: BOTH Normal dirs, namespaced (numeric names collide) ───────
    neg_tags = {config.NORMAL_DIR: "neg_normal",
                config.NORMAL_NONCANCER_DIR: "neg_noncancer"}
    n_neg = {}
    for ndir in config.NEGATIVE_DIRS:
        tag = neg_tags[ndir]
        c = 0
        for img in list_images(ndir):
            _place(root, "train", img, "", f"{tag}__{img.name}")
            c += 1
        n_neg[tag] = c

    # ── locked test/ → test split (binary), eval only ────────────────────────
    n_test = 0
    for img in list_images(config.TEST_IMAGES):
        _place(root, "test", img,
                _binary_label_text(label_path_for(img, config.TEST_LABELS)),
                f"test__{img.name}")
        n_test += 1

    # ── Roboflow: convert (all sources/splits) → dedup → web_holdout ─────────
    converted: dict[str, dict[str, list]] = {}
    all_roboflow_imgs: list[Path] = []
    for src in DATASETS:
        converted[src.name] = {}
        for sp in SPLITS:
            recs = convert_split(src, sp)
            converted[src.name][sp] = recs
            all_roboflow_imgs.extend(r.src_image for r in recs)

    excluded, dedup = build_report(
        all_roboflow_imgs,
        list_images(config.TEST_IMAGES),
        list_images(config.POOL_IMAGES),
    )
    write_report(dedup)

    _fresh_tree(config.WEB_HOLDOUT_DIR, splits=("",))
    wh_img = config.WEB_HOLDOUT_DIR / "images"
    wh_lbl = config.WEB_HOLDOUT_DIR / "labels"

    rf_train_pos = rf_train_neg = 0
    wh_pos = wh_neg = 0
    n_excluded = 0
    sidecar: list[dict] = []

    for src in DATASETS:
        valid_recs = converted[src.name].get("valid", [])
        holdout = valid_holdout_stems(
            valid_recs, config.WEB_HOLDOUT_VALID_FRAC, config.SPLIT_SEED
        )
        for sp in SPLITS:
            for r in converted[src.name].get(sp, []):
                if str(r.src_image.resolve()) in excluded:
                    n_excluded += 1
                    continue

                to_holdout = sp == "test" or (
                    sp == "valid" and r.dst_stem in holdout
                )
                if to_holdout:
                    shutil.copy2(r.src_image, wh_img / r.dst_name)
                    (wh_lbl / f"{r.dst_stem}.txt").write_text(r.label_text)
                    if r.is_negative:
                        wh_neg += 1
                    else:
                        wh_pos += 1
                    continue

                # destined for treatment TRAIN (train + non-holdout valid).
                # sidecar provenance is deterministic → recorded in BOTH arms.
                sidecar.extend(r.sidecar)
                if arm == "plus_roboflow":
                    _place(root, "train", r.src_image, r.label_text,
                           r.dst_name)
                    if r.is_negative:
                        rf_train_neg += 1
                    else:
                        rf_train_pos += 1

    config.DISEASE_SIDECAR.write_text(json.dumps(sidecar, indent=2))
    _write_data_yaml(
        config.WEB_HOLDOUT_DIR,
        {"train": "images", "val": "images", "test": "images"},
    )

    # ── per-arm data.yaml + splits.json (pool-only, identical across arms) ────
    _write_data_yaml(
        root, {"train": "images/train", "val": "images/val",
               "test": "images/test"}
    )
    config.splits_json_for(arm).write_text(json.dumps(split, indent=2))

    return {
        "arm": arm,
        "dataset_root": str(root),
        "pool_total": len(split["train"]) + len(split["val"]),
        "train_pool": len(split["train"]),
        "val_pool": len(split["val"]),
        "negatives_in_train": n_neg,
        "negatives_total": sum(n_neg.values()),
        "test_images": n_test,
        "roboflow_excluded_test_leak": n_excluded,
        "roboflow_train_positives_added": rf_train_pos,
        "roboflow_train_negatives_added": rf_train_neg,
        "web_holdout_positives": wh_pos,
        "web_holdout_negatives": wh_neg,
        "web_holdout_total": wh_pos + wh_neg,
        "pool_near_dupes_reported": dedup["pool_near_dupes_count"],
        "sidecar_entries": len(sidecar),
    }
